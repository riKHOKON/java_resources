package java_basic.arithmetic_operator_and_expression;

public class MathClass {

	public static void main(String[] args) {
	
		int number = -100;
		System.out.println(Math.abs(number));
		System.out.println(Math.sqrt(Math.abs(number)));
		int number2 = 30;
		System.out.println(Math.max(number, number2));
		System.out.println(Math.round(3.1415));
		
	}

}
